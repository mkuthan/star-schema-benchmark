SELECT
    C_CITY,
    S_CITY,
    EXTRACT(YEAR FROM __time) AS "year",
    SUM(LO_REVENUE) AS revenue
FROM "star_schema_benchmark"
WHERE (C_CITY = 'UNITED KI1' OR C_CITY = 'UNITED KI5') AND (S_CITY = 'UNITED KI1' OR S_CITY = 'UNITED KI5')
  AND EXTRACT(YEAR FROM __time) = 1997 AND EXTRACT(MONTH FROM __time) = 12
GROUP BY C_CITY, S_CITY, 3
ORDER BY 3 ASC, revenue DESC
